    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        AlternativaCorreta[qtdPerguntas] = r;
        StringBuffer S = new StringBuffer(jTextArea1.getText());
        for(int i = 0;i<S.length();i++){
            if(S.charAt(i) == '\n')S.setCharAt(i,' ');
        }
        jTextArea1.setText(S.toString());
        Perguntas[qtdPerguntas][0] = jTextArea1.getText();
        Perguntas[qtdPerguntas][1] = jTextField2.getText();
        Perguntas[qtdPerguntas][2] = jTextField3.getText();
        Perguntas[qtdPerguntas][3] = jTextField4.getText();
        Perguntas[qtdPerguntas][4] = jTextField5.getText();
        Perguntas[qtdPerguntas][5] = jTextField6.getText();
        qtdPerguntas++;
        Update();
    }//GEN-LAST:event_jToggleButton1ActionPerformed


