    public static String Ecrypt(String frase){
        if(frase == null)return null;
        StringBuilder Ecr = new StringBuilder(frase);
        for(int i = 0;i<frase.length();i++){
            Ecr.setCharAt(i, (char) (Ecr.charAt(i) + 3));
        }        
        return Ecr.toString();
    }
    private void Update(){
        jComboBox3.removeAllItems();
        for(int i = 0;i<qtdPerguntas;i++){
            StringBuffer S = new StringBuffer("                                             ");
            for(int j = 0;j<Math.min(45,Perguntas[i][0].length());j++){
               S.setCharAt(j, Perguntas[i][0].charAt(j));
            }
            jComboBox3.addItem(S.toString());
        }
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jComboBox3.getSelectedIndex() == -1)return ;
        int atual = jComboBox3.getSelectedIndex();
        for(int i = atual;i<qtdPerguntas-1;i++){
            AlternativaCorreta[i] = AlternativaCorreta[i+1];
            for(int j = 0;j<6;j++){
                Perguntas[i][j] = Perguntas[i+1][j];
            }
        }
        qtdPerguntas--;
        this.Update();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            PrintWriter out = new PrintWriter("config.txt");
            out.println(Ecrypt((jComboBox1.getSelectedIndex()+4)+""));
            out.println(Ecrypt((Integer.parseInt(jTextField1.getText())*60)+""));
            out.println(Ecrypt(((String)jComboBox2.getSelectedItem())+""));
            out.println(Ecrypt(qtdPerguntas+""));
            
            for(int i = 0;i<qtdPerguntas;i++){
                
                for(int j = 0;j<6;j++){
                    out.println(Ecrypt(Perguntas[i][j]+""));
                }
                out.println(Ecrypt(AlternativaCorreta[i]+""));
            }
           out.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ConfigLabirinto.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

